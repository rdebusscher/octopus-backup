package be.c4j.ee.security.credentials.authentication.mp.filter;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * When added to JAX-RS method or Class, these method(s) aren't check to have security requirements (like requiresUser, OctopusPermission, etc ...)
 */
@Target({ElementType.TYPE, ElementType.METHOD})
@Retention(RetentionPolicy.RUNTIME)
public @interface IgnoreOctopusMPRestFilter {
}
